// 1137-E-第 N 个泰波那契数
/**
 * @param {number} n
 * @return {number}
 */
// T0 = 0, T1 = 1, T2 = 1,当n>2时，满足Tn = Tn-1 + Tn-2 + Tn-3
// 动态规划滚动数组
// i = 3       i = 4
// p q r s  => p q r s
// 0 0 1 1 2   0 1 1 2 4
// 时间复杂度On
// 空间复杂度O1
var tribonacci = function (n) {
  if (n === 0) return 0;
  if (n <= 2) return 1;
  let p = 0,
    q = 0,
    r = 1,
    s = 1;
  for (let i = 3; i <= n; ++i) {
    p = q;
    q = r;
    r = s;
    s = p + q + r;
  }
  return s;
};

